package operadores.operadores_relacionais;

public class operadores_relacionais {
    public static void main(String[] args) {
        
        int valor1 = 1;
        int valor2 = 2;

        System.out.println(valor1 == valor2);
        System.out.println(valor1 != valor2);
        System.out.println(valor1 > valor2);
        System.out.println(valor1 < valor2);
        System.out.println(valor1 >= valor2);
        System.out.println(valor1 <= valor2);
        System.out.println(valor1 ^ valor2);
    }
}
